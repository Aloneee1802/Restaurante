function fnSuccess(msg) {
    Swal.fire(
            '¡Exito!',
            msg,
            'success'
            );
}

function fnInfo(msg) {
    Swal.fire(
            '¡Advertencia!',
            msg,
            'info'
            );
}

function fnError(msg) {
    Swal.fire(
            '¡Error!',
            msg,
            'error'
            );
}